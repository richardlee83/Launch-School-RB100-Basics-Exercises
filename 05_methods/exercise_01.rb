# exercise_01.rb

## Problem - Print Me (Part 1)
# Write a method named print_me that prints I'm printing within the method! 
# when invoked as follows:
#
# print_me

## Solution
def print_me
  puts "I'm printing within the method!"
  return nil
end

print_me      # Output: "I'm printing within the method!"   
              # Return Value: nil
