# exercise_02.rb

## Problem - Print Me (Part 2)
# Write a method named print_me so that 'I'm printing the return value!' is printed 
# when running the following code:
#
# puts print_me

## Solution
def print_me 
  return "I'm printing the return value!"
end

puts print_me     # Output: "I'm printing the return value!"
                  # Return Value: nil
