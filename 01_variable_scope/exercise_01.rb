## Problem  - What's My Value? (Part 1)
# What will the following code print and why? Don't run it until you have tried 
# to answer.
#
# a = 7
# 
# def my_value(b)
#   b += 10
# end
# 
# my_value(a)
# puts a

## Solution
a = 7

def my_value(b)
  b += 10
end

my_value(a)
puts a        # Output: 7   Returns: nil
