## Problem - Breakfast, Lunch, or Dinner? (Part 2)
# What will the following code print? Why? Don't run it until you've attempted to answer.
#
# def meal
#   'Evening'
# end
# 
# puts meal

## Solution
def meal
  'Evening'
end
 
puts meal

# - The code will output the string "Evening".
# - The return value of a method is always the value of the evaluated expression on the
#   last line executed within the method.
# - In this case there is no explicit return statement, however the last line executed
#   in the method is the string "Evening" and therefore that is the return value of the
#   method.
